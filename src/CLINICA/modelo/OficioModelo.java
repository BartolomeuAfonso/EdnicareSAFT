package CLINICA.modelo;

/**
 *
 * @author Probook
 */
public class OficioModelo {
    private String guia, empresa, cartao, nome, data;
    private String valor;

    public OficioModelo() {
    }

    public OficioModelo(String guia, String empresa, String cartao, String nome, String data, String valor) {
        this.guia = guia;
        this.empresa = empresa;
        this.cartao = cartao;
        this.nome = nome;
        this.data = data;
        this.valor = valor;
    }

    public String getGuia() {
        return guia;
    }

    public void setGuia(String guia) {
        this.guia = guia;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getCartao() {
        return cartao;
    }

    public void setCartao(String cartao) {
        this.cartao = cartao;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
    
    
    
}
