    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            flag = 2;
            jTextField1.setText("" + jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jTextField2.setText("" + jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            jTextField3.setText("" + jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
            jTabbedPane1.setSelectedIndex(0);
            jButton1.setText("Editar");
            System.out.println("Flag:" + flag);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    public void limpar() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }

    public void salvar() {
        modeloParametro.setDesignacao(jTextField1.getText());
        modeloParametro.setCodigoFactura(jTextField2.getText());
        modeloParametro.setPercentagem(jTextField3.getText());
        controllerCids.salvarParametro(modeloParametro);
    }

    public void editar(int codigo) {
        modeloParametro.setDesignacao(jTextField1.getText());
        modeloParametro.setCodigoFactura(jTextField2.getText());
        modeloParametro.setPercentagem(jTextField3.getText());
        controllerCids.editarParametro(modeloParametro, codigo);
    }

    public final void mostraFactura(String sql) {
        System.out.println("Teste:" + sql);
        try {
            con = new ConexaoBancos().ConexaoBD();
            PreparedStatement ps;
            ResultSet rs;
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(50);
            jTable1.getColumnModel().getColumn(0).setResizable(false);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
            jTable1.getColumnModel().getColumn(1).setResizable(false);
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(100);
            jTable1.getColumnModel().getColumn(2).setResizable(false);
            jTable1.getColumnModel().getColumn(3).setPreferredWidth(100);
            jTable1.getColumnModel().getColumn(3).setResizable(false);
            jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
            jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            model.setNumRows(0);
            while (rs.next()) {
                model.addRow(new String[]{rs.getString("p.codParametro"), rs.getString("p.descricao"), rs.getString("p.valor"),
                    rs.getString("p.percentagem")});
            }
        } catch (Exception ex) {
            System.out.println("Erro!!!" + ex);
        }

    }

