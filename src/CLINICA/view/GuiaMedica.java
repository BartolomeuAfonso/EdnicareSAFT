    public void limpar() {
        jTextArea2.setText("");
        jTextArea1.setText("");
    }

    public int getCodigoPaciente() {
        return controllerUtente.getCodigoUtente(codigoPaciente);
    }

    public int getCodigoHospital() {
        return controllerCids.getCodigoHospital(jComboBox1.getSelectedItem().toString());
    }

    public void salvar() {

        transferenciaMedica.setCodigoMedico(codigoMedico);
        transferenciaMedica.setCodigoHospital(getCodigoHospital());
        transferenciaMedica.setCodigoPaciente(getCodigoPaciente());
        transferenciaMedica.setMotivo(jTextArea2.getText());
        transferenciaMedica.setTratamento(jTextArea1.getText());
        controllerTransferencia.salvarTransferencia(transferenciaMedica);
    }

