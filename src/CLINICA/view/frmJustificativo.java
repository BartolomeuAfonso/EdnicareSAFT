    public String getDataInicioReposuo() {
        GregorianCalendar gc = new GregorianCalendar();
        gc.setTime(dataInicio.getDate());
        String dataSelecionada = gc.get(GregorianCalendar.YEAR) + "-"
                + (gc.get(GregorianCalendar.MONTH) + 1) + "-"
                + gc.get(GregorianCalendar.DATE);

        return dataSelecionada;
    }

    public String getDataFimRepouso() {
        GregorianCalendar gc = new GregorianCalendar();
        gc.setTime(dataFim.getDate());
        String dataSelecionada = gc.get(GregorianCalendar.YEAR) + "-"
                + (gc.get(GregorianCalendar.MONTH) + 1) + "-"
                + gc.get(GregorianCalendar.DATE);

        return dataSelecionada;
    }

    public void salvar() {
        justificativoMedico.setDesignacao(getJusficacao());
        justificativoMedico.setGrau(jComboBox2.getSelectedItem().toString());
        justificativoMedico.setAcompanhante(getAcompanhante());
        justificativoMedico.setCodigoCids(getCodigoCids());
        justificativoMedico.setCodigoMedico(getCodigoMedico());
        justificativoMedico.setCodigoPaciente(getCodigoPaciente());
        justificativoMedico.setDatafim(getDataFimRepouso());
        controllerJustificativo.salvar(justificativoMedico);

    }

