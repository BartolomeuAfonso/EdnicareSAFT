    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jLabel10.getText().equals(jComboBox1.getSelectedItem().toString())) {
            salvar();
            int codigo = controllerJustificativo.getLastCodigo();
            if (!jTextField2.getText().isEmpty()) {
                relatorioEstatistica.IrelatorioJustificativo(getCodigoPaciente(), codigo);
            } else {
                relatorioEstatistica.IrelatorioJustificativo1(getCodigoPaciente(), codigo);

            }
        } else {
            JOptionPane.showMessageDialog(null, "O nome selecionado não igual ao pretendido");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField1CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextField1CaretUpdate
        if (jRadioButton1.isSelected()) {
            if (!jTextField1.getText().isEmpty()) {
                jComboBox1.setModel(new DefaultComboBoxModel(controllerBeneficiario.getNome(jTextField1.getText()).toArray()));

            }
        } else if (jRadioButton2.isSelected()) {
            if (!jTextField1.getText().isEmpty()) {
                jComboBox1.setModel(new DefaultComboBoxModel(controllerBeneficiario.getCodigo(Integer.parseInt(jTextField1.getText().trim())).toArray()));
            }
        }
    }//GEN-LAST:event_jTextField1CaretUpdate

