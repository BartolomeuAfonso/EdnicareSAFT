        int linha = jTable1.getSelectedRow();
        if (linha != -1) {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "AtenÃ§Ã£o", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                int codigo = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                controllerMedico.eliminarColaboradores(codigo);
                mostraMedico("select m.idmedicoColaboradores, m.nomeCompleto,m.morada,m.contacto,e.designacao from\n"
                        + "medicocolaboradores m inner join especialidade_medico e on m.codigoEspecialidade=e.idEspecialidade where m.codigoStatus=1 order by 2");

            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um produto na tabela!");
        }

