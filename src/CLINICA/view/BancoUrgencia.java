    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (!jTextField1.getText().isEmpty()) {
            controllerBancoUrgencia.salvarServico(getDadoConsumivel());
              jTextArea5.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "O campo nome deve ser Preenchido");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (!jTextField1.getText().isEmpty()) {
            controllerBancoUrgencia.salvarProcedimento(getDadoProcedimento());
            jTextArea6.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "O campo nome deve ser Preenchido");
        }
    }//GEN-LAST:event_jButton6ActionPerformed
    
    public bancoUrgenciaModelo getDadoConsumivel() {
        urgenciaModelo.setDesignacao(jTextArea5.getText());
        urgenciaModelo.setPaciente(jTextField1.getText());
        urgenciaModelo.setAcompanhte(jTextField4.getText());
        urgenciaModelo.setCodigoUtilizador(codigoUtilizador);
        return urgenciaModelo;
    }
    
    public bancoUrgenciaModelo getDadoProcedimento() {
        urgenciaModelo.setDesignacao(jTextArea6.getText());
        urgenciaModelo.setPaciente(jTextField1.getText());
        urgenciaModelo.setAcompanhte(jTextField4.getText());
        urgenciaModelo.setCodigoUtilizador(codigoUtilizador);
        return urgenciaModelo;
    }

