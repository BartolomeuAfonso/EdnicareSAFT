    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (flag == 0) {
            setModelo();
            try {
                if (control.salvarFornecedor(modelo)) {
                    JOptionPane.showMessageDialog(rootPane, "Dados inseridos!!!");
                    limpar();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao inserir!!!!");
                }
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(frmFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(frmFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            setModelo();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

