            BigDecimal res1 = new BigDecimal(produtoController.calculoVNSP1(codProduto));
            jTextField10.setText("" + Double.parseDouble(res.setScale(2, BigDecimal.ROUND_DOWN).toString()));
            jTextField5.setText("" + Double.parseDouble(res1.setScale(2, BigDecimal.ROUND_HALF_DOWN).toString()) + " dia(s)");
            jTextField9.setText("" + util.getItemsVendidos(codProduto));
