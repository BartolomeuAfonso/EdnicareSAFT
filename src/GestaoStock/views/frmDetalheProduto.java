            jTextField7.setText("" + produtoController.getEstoqueMin(codProduto));
            jTextField8.setText("" + produtoController.getEstoqueCritico(codProduto));
            jTextField2.setText("" + entradaController.getLoteProduto(codProduto));
            jTextField4.setText("" + entradaController.getPrasoValidade(codProduto) + " dia(s)");
            jTextField1.setText("" + entradaController.getCentroCusto(codProduto));
            jTextField3.setText("" + entradaController.getUnidade(codProduto));
