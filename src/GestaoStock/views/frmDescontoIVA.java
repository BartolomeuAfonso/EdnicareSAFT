    public void salvar() throws UnsupportedEncodingException {
        try {
            tabelaIVa.setDesignacao(jTextField1.getText());
            tabelaIVa.setIva(Double.valueOf(jTextField2.getText()));
            controller.salvarCategoria(tabelaIVa);

        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(frmDescontoIVA.class.getName()).log(Level.SEVERE, null, ex);
        }
        MostrarProdutos();
    }

    public void MostrarProdutos() {
        String sql = "SELECT * FROM tabelaiva";
        ConexaoBancos conexao = new ConexaoBancos();
        try {
            conexao.Connectando();
            PreparedStatement ps;
            ResultSet rs;
            ps = conexao.conn.prepareStatement(sql);
            rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(50);
            jTable1.getColumnModel().getColumn(0).setResizable(false);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
            jTable1.getColumnModel().getColumn(1).setResizable(false);
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(100);
            jTable1.getColumnModel().getColumn(2).setResizable(false);
            jTable1.getTableHeader().setReorderingAllowed(false);
            jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);
            jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            model.setNumRows(0);
            while (rs.next()) {
                model.addRow(new String[]{rs.getString("idIVA"), rs.getString("designacao"), rs.getString("iva")});
            }

        } catch (Exception ex) {
            System.out.println("Erro!!!" + ex);
        }

    }

