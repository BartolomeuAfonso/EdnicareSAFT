    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            estoqueUN = Integer.parseInt((jTextField3.getText()));
            estoqueCX = estoque.getCXProduto(codproduto);
            produto.setEstoqueAtual(estoqueUN);
            produto.setCodProduto(codproduto);
            estoque.operarEstoque2(estoqueCX, estoqueUN, codproduto);
            produtos.atualizarEstoqueAtual(produto);
            mov.setData(Data.getDataActual());
            mov.setTipo(3);//2-Saida e 1- Entrada e 3-Actualizacao
            mov.setItem((jComboBox1.getSelectedItem().toString()));
            mov.setQtdAnteriorCX(estoqueCX);
            mov.setQtdAnteriorUN(estoque.getUnidadeProduto(codproduto));
            mov.setQtdMovimentadaCX(0);
            mov.setQtdMovimentadaUN(Integer.parseInt((jTextField3.getText())));
            mov.setQtdDisponivelCX(estoqueCX + 0);
            mov.setQtdDisponivelUN(estoqueUN);
            mov.setCodUtilizador(1);
            movimentacoes.salvarMovimentacoes(mov);
            JOptionPane.showMessageDialog(rootPane, "Quantidade reduzida para :" + estoqueUN);
        } catch (SQLException ex) {
            Logger.getLogger(frmAtualizarEstoque.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(frmAtualizarEstoque.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(frmAtualizarEstoque.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

