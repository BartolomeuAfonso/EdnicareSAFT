            System.out.println("Nome :" + nome);
            System.out.println("estoque atual :" + st);
            System.out.println("Venda Média Diária :" + vmd);
            System.out.println("VNSP :" + vnsp);
            BigDecimal res = new BigDecimal(vmd);
            BigDecimal res1 = new BigDecimal(vnsp);
            inserirTabela(cod, nome, Double.parseDouble(res.setScale(2, BigDecimal.ROUND_HALF_UP).toString()), Double.parseDouble(res1.setScale(2, BigDecimal.ROUND_HALF_UP).toString())) ;
