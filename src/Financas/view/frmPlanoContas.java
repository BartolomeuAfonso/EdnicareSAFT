    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        rown = jTable1.rowAtPoint(evt.getPoint());

        int column = jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / jTable1.getRowHeight();

        if (row < jTable1.getRowCount() && row >= 0 && column < jTable1.getColumnCount() && column >= 0) {
            Object value = jTable1.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                String va1 = "" + jTable1.getValueAt(rown, 0);
                String va2 = "" + jTable1.getValueAt(rown, 1);
                String va3 = "" + jTable1.getValueAt(rown, 2);
                String va4 = "" + jTable1.getValueAt(rown, 3);
                String va5 = "" + jTable1.getValueAt(rown, 4);
                if (boton.getName().equals("edit")) {
//                    int opc = JOptionPane.showConfirmDialog(rootPane, "<html><font color = \"#08088A\"> <left> Atenção, você estas prestes a alterar a Conta <br> Se for alterar o NOME DA CONTA terá<br>que ajustar todas as contas correspondentes a este centro de custo,<br>se tiver dúvida, por favor consultar o administrador do sistema.<br><br>Deseja mesmo alterar o Centro de Custo? </br></html>", "Aviso do Sistema", 1);
//                    if (opc == 0) {
                    this.setEnabled(false);
                    frmCadastroContas hst = new frmCadastroContas(conexao, this, 1, va5);
                    hst.setVisible(true);
                    hst.toFront();
                    getEnviarDados();

//                    } else {
//
//                    }
                } else if (boton.getName().equals("v")) {
                    try {
                        JOptionPane.showMessageDialog(rootPane, "Estamos verificando se existem lançamentos dessa conta no Movimento de Caixa...");
                        new frmAlertaExcluzao(conexao, "Deseja realmente excluir a Conta?", "Código.:", va1, "Registro exluido!!!", "conta").setVisible(true);
                    } catch (Exception ex) {
                        System.out.println(ex.getMessage());
                    }

                }
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

