    private void jTableRecibosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableRecibosMouseClicked
        rown = jTableRecibos.rowAtPoint(evt.getPoint());

        int column = jTableRecibos.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / jTableRecibos.getRowHeight();

        String va1 = "" + jTableRecibos.getValueAt(rown, 0);
        String va2 = "" + jTableRecibos.getValueAt(rown, 1);
        String va3 = "" + jTableRecibos.getValueAt(rown, 2);
        String va4 = "" + jTableRecibos.getValueAt(rown, 3);
        String va5 = "" + jTableRecibos.getValueAt(rown, 4);
        String va6 = "" + jTableRecibos.getValueAt(rown, 5);

        if (evt.getClickCount() == 2) {
            aux1 = va5.replaceAll(",00", "");
            aux2 = removerPonto(aux1);
            valor = Double.parseDouble(aux2);
//            this.setEnabled(false);
            frmRecibos hst = new frmRecibos(conn, 1, valor, c_recibos.getConta(Integer.parseInt(va1)), c_recibos.getCaixa(Integer.parseInt(va1)), va2, va6, aux1, va1);
            hst.setVisible(true);
//            hst.toFront();

        } else {
            if (row < jTableRecibos.getRowCount() && row >= 0 && column < jTableRecibos.getColumnCount() && column >= 0) {
                Object value = jTableRecibos.getValueAt(row, column);
                if (value instanceof JButton) {
                    ((JButton) value).doClick();
                    JButton boton = (JButton) value;

                    if (boton.getName().equals("edit")) {
                        aux1 = va5.replaceAll(",00", "");
                        aux2 = removerPonto(aux1);
                        valor = Double.parseDouble(aux2);
//                        this.setEnabled(false);
                        frmRecibos hst = new frmRecibos(conn, 1, valor, c_recibos.getConta(Integer.parseInt(va1)), c_recibos.getCaixa(Integer.parseInt(va1)), va2, va6, aux1, va1);
                        hst.setVisible(true);
//                        hst.toFront();
                    }
                    if (boton.getName().equals("delete")) {
                        try {
                            new frmAlertaExcluzao(conn, "Deseja realmente excluir esse RECIBO?", va1, va2, va5, "recibo", "").setVisible(true);
                        } catch (Exception ex) {
                            System.out.println(ex.getMessage());
                        }

                    }
                    if (boton.getName().equals("print")) {

                    }
                }
            }
        }

    }//GEN-LAST:event_jTableRecibosMouseClicked

