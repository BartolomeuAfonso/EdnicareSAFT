    public static String removerPonto(String num) {
        String novoNumero = "";

        for (int i = 0; i < num.length(); ++i) {
            if (num.charAt(i) == ',') {
                break;
            } else if (num.charAt(i) != '.') {
                novoNumero += num.charAt(i);
            }
        }

        return novoNumero;
    }

    public static void setarTotais() {
        String va4 = "";
        for (int i = 0; i < jTableRecibos.getRowCount(); i++) {
            va4 = "" + jTableRecibos.getValueAt(i, 4);
            aux1 = va4.replaceAll(",00", "");
            aux2 = removerPonto(aux1);
            valor += Double.parseDouble(aux2);
        }
        jTextFieldValor.setText(df.format(valor));

    }

