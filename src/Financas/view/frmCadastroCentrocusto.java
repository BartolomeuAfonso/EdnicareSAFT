    void getTipo() {
        if (flag == 0) {
            controllerTipo = new ctr_TipoCentrocusto(conexao);
            jComboBoxTipo.setModel(new DefaultComboBoxModel(controllerTipo.getAll().toArray()));
        } else {
            System.out.println("tipo " + tipo);
            controllerTipo = new ctr_TipoCentrocusto(conexao);
            jComboBoxTipo.setModel(new DefaultComboBoxModel(controllerTipo.getAll2(tipo).toArray()));
            jComboBoxTipo.insertItemAt(tipo, 0);
            jComboBoxTipo.setSelectedIndex(0);
        }
    }

